static inline void lcd_cs_lo() {
    HAL_GPIO_WritePin(GPIOA, LCD_CS_Pin, GPIO_PIN_RESET);

}

static inline void lcd_cs_hi() {
    HAL_GPIO_WritePin(GPIOA, LCD_CS_Pin, GPIO_PIN_SET);

}

static inline void lcd_dc_lo() {
    HAL_GPIO_WritePin(GPIOA, LCD_DC_Pin, GPIO_PIN_RESET);
}
static inline void lcd_dc_hi() {
    HAL_GPIO_WritePin(GPIOA, LCD_DC_Pin, GPIO_PIN_SET);
}

static inline void lcd_reset_lo() {
    HAL_GPIO_WritePin(GPIOA, LCD_RES_Pin, GPIO_PIN_RESET);
}
static inline void lcd_reset_hi() {
	HAL_GPIO_WritePin(GPIOA, LCD_RES_Pin, GPIO_PIN_SET);
}

void LCD_WriteComm(unsigned char comm){
	lcd_dc_lo();
	lcd_cs_lo();
	HAL_SPI_Transmit(&hspi1, &comm, 1, 100);
	lcd_cs_hi();
}

void LCD_WriteData(unsigned char data)
{
	lcd_dc_hi();
	lcd_cs_lo();
	HAL_SPI_Transmit(&hspi1, &data, 1, 100);
	lcd_cs_hi();
}

void LCD_WriteData2(unsigned short data)
{
// Write Data 2 bytes
    unsigned short d;
	lcd_dc_hi();
	lcd_cs_lo();
    d=(data>>8) | (data<<8);
	HAL_SPI_Transmit(&hspi1, (unsigned char *)&d, 2, 100);
	lcd_cs_hi();
}

void LCD_WriteDataN(unsigned char *b,int n)
{
// Write Data N bytes
	lcd_dc_hi();
	lcd_cs_lo();
	HAL_SPI_Transmit(&hspi1, b, n, 100);
	lcd_cs_hi();
}

void LCD_Read(unsigned char com,unsigned char *b,int n){
	lcd_cs_lo();
// Write Command
	lcd_dc_lo();
	HAL_SPI_Transmit(&hspi1, &com, 1, 100);
// Read Data
	lcd_dc_hi();
	HAL_SPI_Receive(&hspi1, 0, b, 100);
	HAL_SPI_Receive(&hspi1, 0, b, 100);
	lcd_cs_hi();
}

