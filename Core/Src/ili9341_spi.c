void circle(int x0,int y0,unsigned int r,unsigned char c)
{
	int x,y,f;
	x=r;
	y=0;
	f=-2*r+3;
	while(x>=y){
		pset(x0-x,y0-y,c);
		pset(x0-x,y0+y,c);
		pset(x0+x,y0-y,c);
		pset(x0+x,y0+y,c);
		pset(x0-y,y0-x,c);
		pset(x0-y,y0+x,c);
		pset(x0+y,y0-x,c);
		pset(x0+y,y0+x,c);
		if(f>=0){
			x--;
			f-=x*4;
		}
		y++;
		f+=y*4+2;
	}
}
