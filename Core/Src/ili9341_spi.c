void putfont(int x,int y,unsigned char c,int bc,unsigned char n)

{
	int i,j;
	unsigned char d;
	int skip;
	unsigned short c1;
	const unsigned char *p;
	static unsigned char lcddatabuf[16];
	unsigned char *lcdbufp;
	if(x<=-8 || x>=X_RES || y<=-8 || y>=Y_RES) return;
	if(y<0){
		i=0;
		p=FontData+n*8-y;
	}
	else{
		i=y;
		p=FontData+n*8;
	}
	c1=palette[c];
	if(bc>=0) bc=palette[bc];
	for(;i<y+8;i++){
		if(i>=Y_RES) return;
		d=*p++;
		if(x<0){
			j=0;
			d<<=-x;
		}
		else{
			j=x;
		}
		if(bc<0){
			skip=1;
			for(;j<x+8;j++){
				if(j>=X_RES){
					break;
				}
				if(d&0x80){
					if(skip){
						LCD_SetCursor(j,i);
						skip=0;
					}
					LCD_WriteData2(c1);
				}
				else skip=1;
				d<<=1;
			}
		}
		else{
			LCD_SetCursor(j,i);
			lcdbufp=lcddatabuf;
			for(;j<x+8;j++){
				if(j>=X_RES){
					break;
				}
				if(d&0x80){
					*lcdbufp++=c1>>8;
					*lcdbufp++=(unsigned char)c1;
				}
				else{
					*lcdbufp++=bc>>8;
					*lcdbufp++=(unsigned char)bc;
				}
				d<<=1;
			}
			if(lcdbufp!=lcddatabuf) LCD_WriteDataN(lcddatabuf,lcdbufp-lcddatabuf);
		}
	}
}

void printstr(int x,int y,unsigned char c,int bc,unsigned char *s){
	while(*s){
		putfont(x,y,c,bc,*s++);
		x+=8;
	}
}
void printnum(int x,int y,unsigned char c,int bc,unsigned int n){
	unsigned int d,e;
	d=10;
	e=0;
	while(n>=d){
		e++;
		if(e==9) break;
		d*=10;
	}
	x+=e*8;
	do{
		putfont(x,y,c,bc,'0'+n%10);
		n/=10;
		x-=8;
	}while(n!=0);
}
void printnum2(int x,int y,unsigned char c,int bc,unsigned int n,unsigned char e){
	if(e==0) return;
	x+=(e-1)*8;
	do{
		putfont(x,y,c,bc,'0'+n%10);
		e--;
		n/=10;
		x-=8;
	}while(e!=0 && n!=0);
	while(e!=0){
		putfont(x,y,c,bc,' ');
		x-=8;
		e--;
	}
}

