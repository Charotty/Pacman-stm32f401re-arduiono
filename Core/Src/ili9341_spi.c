{
	int i,j,k;
	if(x<=-m || x>X_RES || y<=-n || y>=Y_RES) return;
	if(y<0) i=0;
	else i=y;
	if(x<0) j=0;
	else j=x;
	if(x+m>=X_RES) k=X_RES-j;
	else k=x+m-j;
	for(;i<y+n;i++){
		if(i>=Y_RES) return;
		LCD_continuous_output(j,i,0,k);
	}
}

void gline(int x1,int y1,int x2,int y2,unsigned char c)
{
	int sx,sy,dx,dy,i;
	int e;

	if(x2>x1){
		dx=x2-x1;
		sx=1;
	}
	else{
		dx=x1-x2;
		sx=-1;
	}
	if(y2>y1){
		dy=y2-y1;
		sy=1;
	}
	else{
		dy=y1-y2;
		sy=-1;
	}
	if(dx>=dy){
		e=-dx;
		for(i=0;i<=dx;i++){
			pset(x1,y1,c);
			x1+=sx;
			e+=dy*2;
			if(e>=0){
				y1+=sy;
				e-=dx*2;
			}
		}
	}
	else{
		e=-dy;
		for(i=0;i<=dy;i++){
			pset(x1,y1,c);
			y1+=sy;
			e+=dx*2;
			if(e>=0){
				x1+=sx;
				e-=dy*2;
			}
		}
	}
}
