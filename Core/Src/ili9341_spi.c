void putbmpmn(int x,int y,unsigned char m,unsigned char n,const unsigned char bmp[])

{
	int i,j;
	int skip;
	const unsigned char *p;
	if(x<=-m || x>X_RES || y<=-n || y>=Y_RES) return;
	if(y<0){
		i=0;
		p=bmp-y*m;
	}
	else{
		i=y;
		p=bmp;
	}
	for(;i<y+n;i++){
		if(i>=Y_RES) return;
		if(x<0){
			j=0;
			p+=-x;
		}
		else{
			j=x;
		}
		skip=1;
		for(;j<x+m;j++){
			if(j>=X_RES){
				p+=x+m-j;
				break;
			}
			if(*p!=0){
				if(skip){
					LCD_SetCursor(j,i);
					skip=0;
				}
				LCD_WriteData2(palette[*p]);
			}
			else skip=1;
			p++;
		}
	}
}


